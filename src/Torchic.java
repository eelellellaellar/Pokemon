public class Torchic extends Pokemon {
    public Torchic(String name, int pokedexNum, double weight, double height, int attack, int defense, int stamina, String type){
        this.name = name;
        this.pokedexNum = pokedexNum;
        this.weight = weight;
        this.height = height;
        this.attack = attack;
        this.defense = defense;
        this.stamina= stamina;
        this.type = type;
    }
}
